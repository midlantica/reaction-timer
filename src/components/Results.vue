<template>
  <p class="rank">Reaction Time: {{ score }}ms</p>
  <p class="rank">{{ rank }}</p>
</template>

<script>
  export default {
    name: 'Results',
    props: ['score'],
    data() {
      return {
        rank: null
      }
    },
    mounted() {
      if (this.score < 250) {
        this.rank = "Ninja Fingers! "
      } else if (this.score < 400) {
        this.rank = 'Rapid Reflexes!'
      } else {
        this.rank = 'Snail Reflexes!'
      }
    }
  }
</script>

<style>
  .rank {
    font-size: 1.4rem;
    color: rgb(142, 177, 149);
    font-weight: bold;
  }
</style>
